// primo gruppo

var button = document.getElementById("uno");
var audio = document.getElementById("audio1");

button.addEventListener("click", function(){
    document.getElementById("uno").classList.toggle("bg-none");
  if(audio.paused){
    audio.play();
  } else {
    audio.pause(); 
  }
});


